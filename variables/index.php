<?php 
	//Enteros - int 
	$intVar = 1;
	var_dump($intVar); // Nos indica el tipo y valor de la variable

	//Flotante - float
	$floatVar = 1.4;
	var_dump($floatVar);

	//Cadena - string
	$stringVar = 'Hola Mundo';
	var_dump($stringVar);

	//Boleano - boolean
	$boolVar = true || false;
	var_dump($boolVar);

	//Nulo - null
	$nullVar = null;
	var_dump($nullVar);

	//Arreglo - array
	$arrayVar = [1, 'fornite', 3.7, false];
	var_dump($arrayVar);
?>